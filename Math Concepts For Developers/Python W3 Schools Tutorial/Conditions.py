#Simple condition:
a = 10;
b = 2;

if a > b:
    print("a is bigger than b");

#Using the elif keyword:
a = 33
b = 33
if b > a:
  print("b is greater than a");
elif a == b:
  print("a and b are equal");

#Using the else keyword:
a = 200
b = 33
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")
else:
  print("a is greater than b")